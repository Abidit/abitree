def get_icon(name: str, is_dir: bool = False) -> str:
    if is_dir:
        return ICONS["__folder__"]

    # Check full filename first (e.g. Dockerfile, Makefile)
    if name in ICONS:
        return ICONS[name]

    # Check extension
    ext = "." + name.rsplit(".", 1)[-1] if "." in name else ""
    return ICONS.get(ext, ICONS["__default__"])
